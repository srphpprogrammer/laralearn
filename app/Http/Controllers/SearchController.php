     */
    public function index(Request $request)
    {   

        $friends = Friend::getFriends();
        $friends[] = Auth::id();
        $users = User::whereNotIn('id',$friends)->where('name', 'like', '%' . $request->input('q') . '%')->orderBy('id','desc')
->paginate(8);
        # dd($users);
       return view('search',['users'=>$users]);
    }

